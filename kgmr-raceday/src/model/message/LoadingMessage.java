package model.message;

/**
 * Loading message class for the message thrown before the start.
 * 
 * @author Ken Gil Romero kgmr@uw.edu
 * @version Fall 18 
 */
public class LoadingMessage extends AbstractMessage {
    /**
     * 
     * @param theMessage the message passed
     */
    public LoadingMessage(final String theMessage) {
        super(theMessage, -1);
    }

    /**
     * 
     * @param theMessage the message passed
     * @param theTimestamp the timestamp passed
     */
    public LoadingMessage(final String theMessage, final int theTimestamp) {
        super(theMessage, theTimestamp);
        // TODO Auto-generated constructor stub
    }

    @Override
    public String getType() {
        // TODO Auto-generated method stub
        return "Loading";
    }
}
