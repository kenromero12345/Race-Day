package model.message;

/**
 * Crossing message concrete class.
 * 
 * @author Ken Gil Romero kgmr@uw.edu
 * @version Fall 18 
 */
public class CrossingMessage extends AbstractMessageWithId {
  
    /**
     * Crossing message constructor.
     * 
     * @param theMessage the message passed
     * @param theTimestamp the timestamp passed
     * @param theId the id passed
     */
    public CrossingMessage(final String theMessage, 
                           final int theTimestamp, 
                           final int theId) {
        super(theMessage, theTimestamp, theId);
    }

    @Override
    public String getType() {
        return "Crossing";
    }

}
